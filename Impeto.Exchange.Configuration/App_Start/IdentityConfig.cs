    public class EmailService : IIdentityMessageService
    {
        private string _smtpServerName = ConfigurationManager.AppSettings["SmtpServerName"];
        private string _smtpFromAddress = ConfigurationManager.AppSettings["SmtpFromAddress"];
        private string _smtpPassword = ConfigurationManager.AppSettings["SmtpPassword"];
        private int _smtpPort = Convert.ToInt32(ConfigurationManager.AppSettings["SmtpPort"]);

        public Task SendAsync(IdentityMessage message)
        {
            var client = new SmtpClient
            {
                Host = _smtpServerName,
                Port = _smtpPort,
                
                Credentials = new NetworkCredential(_smtpFromAddress, _smtpPassword),
                EnableSsl = true,
            };

            var @from = new MailAddress(_smtpFromAddress);
            var to = new MailAddress(message.Destination);

            var mail = new MailMessage(@from, to)
            {
                Subject = message.Subject,
                Body = message.Body,
                IsBodyHtml = true,
            };

            client.Send(mail);

            return Task.FromResult(0);
        }
    }

